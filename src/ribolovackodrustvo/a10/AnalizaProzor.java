    private void populate() {
        try {
            Statement s = c.createStatement();
            ResultSet rs = s.executeQuery("SELECT * FROM Pecaros");
            DefaultComboBoxModel<PecarosDO> dcbm = new DefaultComboBoxModel<>();
            while (rs.next()) {
                PecarosDO pecaros = new PecarosDO();
                pecaros.ID = rs.getInt("PecarosID");
                pecaros.ime = rs.getString("Ime");
                pecaros.prezime = rs.getString("Prezime");
                dcbm.addElement(pecaros);
            }
            jComboBox1.setModel(dcbm);
        } catch (SQLException ex) {
            Logger.getLogger(AnalizaProzor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

