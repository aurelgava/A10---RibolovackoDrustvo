    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            int dan1, mes1, god1, dan2, mes2, god2;
            dan1 = Integer.parseInt((String) jComboBox2.getSelectedItem());
            mes1 = Integer.parseInt((String) jComboBox3.getSelectedItem());
            god1 = Integer.parseInt((String) jComboBox4.getSelectedItem());
            dan2 = Integer.parseInt((String) jComboBox5.getSelectedItem());
            mes2 = Integer.parseInt((String) jComboBox6.getSelectedItem());
            god2 = Integer.parseInt((String) jComboBox7.getSelectedItem());
            LocalDate datumOd = LocalDate.of(god1, mes1, dan1);
            LocalDate datumDo = LocalDate.of(god2, mes2, dan2);
            PreparedStatement ps = c.prepareStatement("SELECT Vrsta_Ribe.Naziv AS Vrsta, COUNT(Ulov.RbrUlova) AS Broj\n"
                    + "FROM (Vrsta_Ribe INNER JOIN Ulov ON Vrsta_Ribe.VrstaID = Ulov.VrstaID)\n"
                    + "INNER JOIN Pecaros ON Pecaros.PecarosID = Ulov.PecarosID\n"
                    + "WHERE PecarosID = ? AND  (Ulov.Datum BETWEEN ? AND ? )\n"
                    + "GROUP BY Vrsta_Ribe.Naziv");
            ps.setInt(1, ((PecarosDO) jComboBox1.getSelectedItem()).ID);
            ps.setDate(2, Date.valueOf(datumOd));
            ps.setDate(3, Date.valueOf(datumDo));
            ResultSet rs = ps.executeQuery();

            DefaultTableModel dtm = new DefaultTableModel();
            DefaultPieDataset dps = new DefaultPieDataset();
            dtm.addColumn("Vrsta");
            dtm.addColumn("Broj");
            while (rs.next()) {
                Object[] red = new Object[2];
                red[0] = rs.getString("Vrsta");
                red[1] = rs.getInt("Broj");
                dtm.addRow(red);
                dps.setValue(rs.getString("Vrsta"), rs.getInt("Broj"));
            }
            jTable1.setModel(dtm);
            JFreeChart chart = ChartFactory.createPieChart3D("", dps, true, true, false);
            ChartPanel frame = new ChartPanel(chart);
            frame.setVisible(true);
            frame.setPreferredSize(new Dimension(500, 270));
            jPanel1.setLayout(new java.awt.BorderLayout());
            jPanel1.add(frame, BorderLayout.CENTER);
            jPanel1.validate();

        } catch (SQLException ex) {
            Logger.getLogger(AnalizaProzor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

